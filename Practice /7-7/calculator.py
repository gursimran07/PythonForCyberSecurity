#!/usr/bin/env python3 

num1 = float(input("Enter the first number\n"))
num2= float(input("Enter the second number\n"))
activity= input("Enter the operator\n")

if activity == "+":
    print(num1+num2)
if activity == "-":
    print(num1-num2)
if activity == "/":
    print(num1/num2)
if activity == "*":
    print(num1*num2)

